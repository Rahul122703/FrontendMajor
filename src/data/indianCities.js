// Helper function to search cities
export const searchCities = (query) => {
  if (!query || query.length < 2) return [];
  const lowerQuery = query.toLowerCase();
  return indianCities.filter(city => 
    city.name.toLowerCase().includes(lowerQuery)
  ).slice(0, 10); // Return top 10 matches
};

// Helper function to calculate distance between two coordinates
export const calculateDistance = (lat1, lon1, lat2, lon2) => {
  const R = 6371; // Earth's radius in km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
};

// Helper function to find nearest data point
export const findNearestDataPoint = (city, data) => {
  if (!data || !Array.isArray(data) || data.length === 0) return null;
  
  let nearest = null;
  let minDistance = Infinity;
  
  data.forEach(point => {
    const distance = calculateDistance(city.lat, city.lng, point.lat, point.lon);
    if (distance < minDistance) {
      minDistance = distance;
      nearest = { ...point, distance };
    }
  });
  
  return nearest;
};
